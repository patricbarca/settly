-- Perfiles: lectura entre usuarios (para invitar); escritura solo el propio
DROP POLICY IF EXISTS "Own profile"     ON profiles;
DROP POLICY IF EXISTS "Read profiles"   ON profiles;
DROP POLICY IF EXISTS "Insert own profile" ON profiles;
DROP POLICY IF EXISTS "Update own profile" ON profiles;
CREATE POLICY "Read profiles"       ON profiles FOR SELECT USING (auth.uid() IS NOT NULL);
CREATE POLICY "Insert own profile"  ON profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "Update own profile"  ON profiles FOR UPDATE USING (auth.uid() = id) WITH CHECK (auth.uid() = id);

-- Grupos
DROP POLICY IF EXISTS "Members view groups"        ON groups;
DROP POLICY IF EXISTS "Members update groups"      ON groups;
DROP POLICY IF EXISTS "Authenticated insert groups" ON groups;
DROP POLICY IF EXISTS "Owner delete groups"        ON groups;
CREATE POLICY "Members view groups" ON groups FOR SELECT
  USING (
    owner_id = auth.uid()
    OR EXISTS (SELECT 1 FROM group_members WHERE group_id = groups.id AND user_id = auth.uid())
    OR EXISTS (SELECT 1 FROM invite_links   WHERE group_id = groups.id AND expires_at > NOW())
  );
CREATE POLICY "Members update groups" ON groups FOR UPDATE
  USING (EXISTS (SELECT 1 FROM group_members WHERE group_id = groups.id AND user_id = auth.uid()));
CREATE POLICY "Authenticated insert groups" ON groups FOR INSERT
  WITH CHECK (auth.uid() = owner_id);
CREATE POLICY "Owner delete groups" ON groups FOR DELETE
  USING (auth.uid() = owner_id);

-- Miembros
-- Helper SECURITY DEFINER: ¿soy miembro de este grupo? Evita la recursión de RLS
-- al consultar group_members dentro de su propia política (ver migrate_v4).
CREATE OR REPLACE FUNCTION public.is_member_of(gid text)
RETURNS boolean LANGUAGE sql SECURITY DEFINER STABLE SET search_path = public AS $$
  SELECT EXISTS (SELECT 1 FROM group_members WHERE group_id = gid AND user_id = auth.uid());
$$;
GRANT EXECUTE ON FUNCTION public.is_member_of(text) TO authenticated;

DROP POLICY IF EXISTS "View own memberships" ON group_members;
DROP POLICY IF EXISTS "View co-members"      ON group_members;
DROP POLICY IF EXISTS "Join group"           ON group_members;
DROP POLICY IF EXISTS "Leave group"          ON group_members;
CREATE POLICY "View own memberships" ON group_members FOR SELECT
  USING (user_id = auth.uid());
-- Leer co-miembros de tus grupos (para "Tu red"/sugeridos). Se combina con la
-- anterior vía OR; no afecta INSERT/DELETE.
CREATE POLICY "View co-members" ON group_members FOR SELECT
  USING (public.is_member_of(group_id));
CREATE POLICY "Join group" ON group_members FOR INSERT
  WITH CHECK (
    (user_id = auth.uid()
      AND EXISTS (SELECT 1 FROM invite_links WHERE group_id = group_members.group_id AND expires_at > NOW()))
    OR (user_id = auth.uid()
      AND EXISTS (SELECT 1 FROM groups WHERE id = group_id AND owner_id = auth.uid()))
    OR (user_id != auth.uid()
      AND EXISTS (SELECT 1 FROM group_members gm WHERE gm.group_id = group_members.group_id AND gm.user_id = auth.uid()))
  );
CREATE POLICY "Leave group" ON group_members FOR DELETE
  USING (user_id = auth.uid());

-- Invite links
DROP POLICY IF EXISTS "Auth users view invites" ON invite_links;
DROP POLICY IF EXISTS "Members create invites"  ON invite_links;
CREATE POLICY "Auth users view invites" ON invite_links FOR SELECT
  USING (auth.uid() IS NOT NULL);
CREATE POLICY "Members create invites" ON invite_links FOR INSERT
  WITH CHECK (
    auth.uid() = created_by
    AND EXISTS (SELECT 1 FROM group_members WHERE group_id = invite_links.group_id AND user_id = auth.uid())
  );

-- Entitlements: el usuario solo lee el suyo (nadie escribe directo)
DROP POLICY IF EXISTS "read own entitlement" ON entitlements;
CREATE POLICY "read own entitlement" ON entitlements FOR SELECT
  USING (user_id = auth.uid());

-- access_codes: RLS activado y SIN políticas → nadie puede leer los códigos
-- Canjes: el usuario ve los suyos
DROP POLICY IF EXISTS "read own redemptions" ON code_redemptions;
CREATE POLICY "read own redemptions" ON code_redemptions FOR SELECT
  USING (user_id = auth.uid());

-- ---------- FUNCIÓN DE CANJE (segura) ----------
CREATE OR REPLACE FUNCTION redeem_access_code(p_code TEXT)
RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  v_uid     UUID := auth.uid();
  v_code    access_codes%ROWTYPE;
  v_expires TIMESTAMPTZ;
BEGIN
  IF v_uid IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'error', 'not_authenticated');
  END IF;

  SELECT * INTO v_code FROM access_codes
   WHERE code = upper(trim(p_code)) FOR UPDATE;

  IF NOT FOUND OR NOT v_code.active THEN
    RETURN jsonb_build_object('ok', false, 'error', 'invalid');
  END IF;
  IF v_code.expires_at IS NOT NULL AND v_code.expires_at < NOW() THEN
    RETURN jsonb_build_object('ok', false, 'error', 'expired');
  END IF;
  IF v_code.max_uses IS NOT NULL AND v_code.uses >= v_code.max_uses THEN
    RETURN jsonb_build_object('ok', false, 'error', 'exhausted');
  END IF;

  IF EXISTS (SELECT 1 FROM code_redemptions WHERE code = v_code.code AND user_id = v_uid) THEN
    RETURN jsonb_build_object('ok', true, 'plan', v_code.grants_plan, 'already', true);
  END IF;

  v_expires := CASE
    WHEN v_code.duration_days IS NULL THEN NULL
    ELSE NOW() + (v_code.duration_days || ' days')::interval
  END;

  INSERT INTO code_redemptions (code, user_id) VALUES (v_code.code, v_uid);
  UPDATE access_codes SET uses = uses + 1 WHERE code = v_code.code;

  INSERT INTO entitlements (user_id, plan, expires_at, source, updated_at)
  VALUES (v_uid, v_code.grants_plan, v_expires, 'code', NOW())
  ON CONFLICT (user_id) DO UPDATE
    SET plan = EXCLUDED.plan, expires_at = EXCLUDED.expires_at,
        source = EXCLUDED.source, updated_at = NOW();

  RETURN jsonb_build_object('ok', true, 'plan', v_code.grants_plan, 'expires_at', v_expires);
END;
$$;

GRANT EXECUTE ON FUNCTION redeem_access_code(TEXT) TO authenticated;

